<h4><em>Programming vs Workshop</em></h4>
<p>Taken from their event calendar, this listing uses the word “workshop” instead of “program”, which is in keeping with Gross’ suggestions.</p>
<img src="images/program1.jpg">
<p>However, in this listing, the word “program” is used multiple times in the last line. This could easily be rewritten to read, “Parents: please be aware that refreshments will be served at this class. A list of ingredients will be available at the class.”</p>
