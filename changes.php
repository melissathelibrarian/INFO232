<h3>Conclusion</h3>

<p>An examination of the LA County Library showed that they are well on their way to utilizing a lexicon that conveys the library’s valuable role as an education center in the community. The language used is clear, concise and steers away from typical library jargon that can cause users to feel lost and overwhelmed.</p>


