<h3><em>Lexicon</em></h3>
<p>As can be seen from reading the Strategic Initiatives, the LA County Library system is already on its way to emphasizing the educational role of libraries. For example, Strategic Initiative 2, Affirm the Library as a Center for Learning - Address our communities’ learning and literacy needs through classes, events and online technology, already uses words such as “learning”, “classes” and “events” to describe the offerings at the library. This initiative even goes so far as to call libraries centers for learning.

Additionally, the Strategic Initiatives refer to library users as “customers”. Gross (2013) asserts that the use of the term “customer” is beneficial to libraries because it “furthers [libraries’] quest to be perceived as a solid financial investment” (p. 37).

The LA County Library System’s Strategic Initiatives do an excellent job at utilizing a lexicon that makes their intended mission sound clear and valuable to the public, without using confusing library jargon. We would recommend only the following small tweaks to the language of Strategic Initiatives 1, 4 and 6:
 </p> 
 <img src="images/initiative4.jpg">

<p>Many libraries use the word “database”, but Gross suggests moving away from this and instead using the phrase “specialized online research tools”, which breaks down what a database actually is and what function it will be providing.</p>
<img src="images/databases.jpg">
<p>On the “Library Locator” section of their webpage, LA County Library lists their three bookmobiles. Gross would suggest that the word “bookmobile” should be swapped out for the phrase “mobile branch”, which conveys to the community that this vehicle has all the same offerings as a brick and mortar branch.</p>
<img src="images/bookmobile.jpg">
</main>
